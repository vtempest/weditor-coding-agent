// Theme color palette definitions for the editor UI and Monaco
// Each theme defines the full color palette used across the UI and Monaco editor.
// CSS custom properties are set from the active theme in page.tsx.

export interface ThemeColors {
  // Backgrounds
  bg0: string;       // main app bg, title bar, status bar
  bg1: string;       // slightly lighter (inactive tabs)
  bg2: string;       // editor / pane bg, breadcrumbs
  bg3: string;       // modal / widget / input bg
  bg4: string;       // line highlight
  border: string;    // borders
  hover: string;     // hover bg
  selection: string; // selection / active highlight

  // Text
  text1: string;     // primary (bright)
  text2: string;     // default
  text3: string;     // secondary
  text4: string;     // muted
  text5: string;     // very muted / disabled

  // Accent
  accent: string;    // primary accent (blue)
  accentHover: string;
  focus: string;     // focus ring

  // Semantic
  added: string;     // git added
  modified: string;  // git modified (= accent usually)
  deleted: string;   // git deleted
  warning: string;   // warnings

  // Syntax highlighting
  syntaxComment: string;
  syntaxKeyword: string;
  syntaxString: string;
  syntaxNumber: string;
  syntaxType: string;
  syntaxFunction: string;
  syntaxVariable: string;
  syntaxOperator: string;
  syntaxDelimiter: string;
  syntaxAttribute: string;
  syntaxTag: string;
  syntaxMacro: string;
  syntaxConstant: string;
  syntaxNamespace: string;

  // Special
  gitBranch: string; // git branch indicator color
  purple: string;    // purple accent (renamed, keywords alt)

  // Scrollbar
  scrollThumb: string;
  scrollThumbHover: string;
}

export interface Theme {
  name: string;
  appearance: "dark" | "light";
  colors: ThemeColors;
}

